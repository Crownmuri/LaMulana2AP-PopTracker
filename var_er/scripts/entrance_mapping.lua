-- Global pairing table: ER_PAIRINGS[codeA] = codeB (bidirectional)
-- This is read by logic.lua for flood-fill routing
ER_PAIRINGS = {}

ENTRANCE_SELECTED = nil

function ClearTargetOverlay(code)
    local target = Tracker:FindObjectForCode("target_" .. code)
    if target then
        target:SetOverlay("")
        target:SetOverlayBackground("#00000000")
    end
end

function SetTargetOverlay(code, destination_code)
    local target = Tracker:FindObjectForCode("target_" .. code)
    if target then
        target:SetOverlayAlign("left")
        target:SetOverlayFontSize(10)
        target:SetOverlay(ER_ENTRANCE_NAMES[destination_code] or destination_code)
        target:SetOverlayBackground("#FF222222")
    end
end

function UnlinkEntrance(code)
    local paired = ER_PAIRINGS[code]
    if not paired then return end

    -- Clear both sides
    ClearTargetOverlay(code)
    ClearTargetOverlay(paired)

    -- Reset toggle states on both entrances
    local objA = Tracker:FindObjectForCode(code)
    local objB = Tracker:FindObjectForCode(paired)
    if objA then objA.Active = false end
    if objB then objB.Active = false end

    ER_PAIRINGS[code] = nil
    ER_PAIRINGS[paired] = nil

    print("Unlinked: " .. (ER_ENTRANCE_NAMES[code] or code) .. " <-> " .. (ER_ENTRANCE_NAMES[paired] or paired))
end

function EntranceClick(code)
    local obj = Tracker:FindObjectForCode(code)
    if not obj then
        print("EntranceClick: could not find object for " .. code)
        return
    end

    -- 1. If clicking the same one again, deselect
    if ENTRANCE_SELECTED == code then
        ENTRANCE_SELECTED = nil
        print("Selection Canceled")
        return
    end

    -- 2. If nothing is selected
    if ENTRANCE_SELECTED == nil then
        -- If already linked, unlink it
        if ER_PAIRINGS[code] then
            UnlinkEntrance(code)
            return
        end
        -- Otherwise select for pairing
        ENTRANCE_SELECTED = code
        print("Source Selected: " .. (ER_ENTRANCE_NAMES[code] or code))
        return
    end

    -- 3. Pair them (Bidirectional A <-> B)
    local codeA = ENTRANCE_SELECTED
    local codeB = code

    -- If either side is already linked, unlink first
    if ER_PAIRINGS[codeA] then UnlinkEntrance(codeA) end
    if ER_PAIRINGS[codeB] then UnlinkEntrance(codeB) end

    -- Store pairing
    ER_PAIRINGS[codeA] = codeB
    ER_PAIRINGS[codeB] = codeA

    -- Update target labels
    SetTargetOverlay(codeA, codeB)
    SetTargetOverlay(codeB, codeA)

    print("Linked: " .. (ER_ENTRANCE_NAMES[codeA] or codeA) .. " <-> " .. (ER_ENTRANCE_NAMES[codeB] or codeB))

    -- Reset selection
    ENTRANCE_SELECTED = nil
end

function InitEntranceWatchers()
    if not ER_ENTRANCE_NAMES then return end
    for code, _ in pairs(ER_ENTRANCE_NAMES) do
        ScriptHost:AddWatchForCode("click_" .. code, code, function(c)
            EntranceClick(c)
        end)
    end
end

InitEntranceWatchers()
