-- Global pairing table: ER_PAIRINGS[codeA] = codeB (bidirectional)
-- This is read by logic.lua for flood-fill routing
ER_PAIRINGS = {}

-- code -> true for entrances locked to their vanilla pairing because their
-- ER category is NOT shuffled in this seed (e.g. Horizontal Entrances OFF).
-- These are pre-filled from slot_data and must be left alone by manual
-- clicks and by the Reveal Spoiler apply/clear passes.
ER_VANILLA = {}

ENTRANCE_SELECTED = nil

-- Frames during which the click watcher should ignore state changes.
-- AddWatchForCode fires for ANY provided-code change — including the programmatic
-- Active toggles that happen during initial state restore, Import, or our own
-- ApplyPairing. Without this guard, a restored Active=true triggers EntranceClick,
-- which sees the (also-restored) pairing and treats it as an unpair click.
ER_SUPPRESS_FRAMES = 0

-- --- Helper: Sync Soul Gate Costs ---
function SyncSoulGateCosts(codeA, codeB)
    local costA = Tracker:FindObjectForCode("cost_" .. codeA)
    local costB = Tracker:FindObjectForCode("cost_" .. codeB)

    if costA and costB then
        -- Match them. If one is set, the other adopts it.
        if costA.CurrentStage > 0 then
            costB.CurrentStage = costA.CurrentStage
        elseif costB.CurrentStage > 0 then
            costA.CurrentStage = costB.CurrentStage
        end
    end
end

-- --- Real-time Cost Sync ---
function OnCostChanged(code)
    -- Get the entrance code by stripping "cost_"
    local gate_code = code:gsub("cost_", "")
    
    -- Find the partner in our pairing table
    local partner_code = ER_PAIRINGS[gate_code]
    
    -- If no partner is paired, do nothing
    if not partner_code then return end

    local my_cost = Tracker:FindObjectForCode(code)
    local partner_cost = Tracker:FindObjectForCode("cost_" .. partner_code)
    
    -- Only update if the partner actually exists and has a different value
    -- (The check prevents an infinite loop between the two paired gates)
    if my_cost and partner_cost and partner_cost.CurrentStage ~= my_cost.CurrentStage then
        partner_cost.CurrentStage = my_cost.CurrentStage
    end
end

function ClearTargetOverlay(code)
    local target = Tracker:FindObjectForCode("target_" .. code)
    if target then
        target:SetOverlay("")
        target:SetOverlayBackground("#00000000")
    end
end

function SetTargetOverlay(code, destination_code)
    local target = Tracker:FindObjectForCode("target_" .. code)
    if target then
        target:SetOverlayAlign("left")
        target:SetOverlayFontSize(10)
        target:SetOverlay(ER_ENTRANCE_NAMES[destination_code] or destination_code)
        target:SetOverlayBackground("#FF222222")
    end
end

function UnlinkEntrance(code)
    local paired = ER_PAIRINGS[code]
    if not paired then return end

    -- Clear both sides
    ClearTargetOverlay(code)
    ClearTargetOverlay(paired)

    -- Reset toggle states on both entrances
    local objA = Tracker:FindObjectForCode(code)
    local objB = Tracker:FindObjectForCode(paired)
    if objA then objA.Active = false end
    if objB then objB.Active = false end

    ER_PAIRINGS[code] = nil
    ER_PAIRINGS[paired] = nil

    if UpdateEscapeRoute then UpdateEscapeRoute() end
end

function EntranceClick(code)
    if ER_SUPPRESS_FRAMES > 0 then return end
    -- Vanilla-locked entrances (unshuffled category) are not manually editable.
    if ER_VANILLA[code] then return end
    local obj = Tracker:FindObjectForCode(code)
    if not obj then return end

    -- 1. If clicking the same one again, deselect
    if ENTRANCE_SELECTED == code then
        ENTRANCE_SELECTED = nil
        return
    end

    -- 2. If nothing is selected
    if ENTRANCE_SELECTED == nil then
        -- If already linked, unlink it
        if ER_PAIRINGS[code] then
            UnlinkEntrance(code)
            return
        end
        -- Otherwise select for pairing
        ENTRANCE_SELECTED = code
        return
    end

    -- 3. Pair them (Bidirectional A <-> B)
    local codeA = ENTRANCE_SELECTED
    local codeB = code

    -- If either side is already linked, unlink first
    if ER_PAIRINGS[codeA] then UnlinkEntrance(codeA) end
    if ER_PAIRINGS[codeB] then UnlinkEntrance(codeB) end

    -- Store pairing
    ER_PAIRINGS[codeA] = codeB
    ER_PAIRINGS[codeB] = codeA

    -- Update target labels
    SetTargetOverlay(codeA, codeB)
    SetTargetOverlay(codeB, codeA)

    -- Sync the costs immediately on pairing
    SyncSoulGateCosts(codeA, codeB)

    -- Reset selection
    ENTRANCE_SELECTED = nil

    if UpdateEscapeRoute then UpdateEscapeRoute() end
end

function InitEntranceWatchers()
    if not ER_ENTRANCE_NAMES then return end
    for code, _ in pairs(ER_ENTRANCE_NAMES) do
        ScriptHost:AddWatchForCode("click_" .. code, code, function(c)
            EntranceClick(c)
        end)
    end
end

InitEntranceWatchers()

-- --- Automatic Watcher Registration ---
-- This loop runs once when the script loads and sets up listeners for all Soul Gates
function InitSoulGateWatchers()
    for code, _ in pairs(ER_ENTRANCE_NAMES) do
        local cost_code = "cost_" .. code
        -- Only add a watch if the item actually exists in the tracker
        if Tracker:FindObjectForCode(cost_code) then
            ScriptHost:AddWatchForCode("sync_watch_" .. cost_code, cost_code, OnCostChanged)
        end
    end
end

-- Call the initialization
InitSoulGateWatchers()

-- ============================================================
-- Persistence: save/restore ER_PAIRINGS across save/load cycles.
-- PopTracker only persists standard item state (Active, stages, counts),
-- so without this the custom entrance pairing table is lost on reload.
-- ============================================================

-- Frame budget that absorbs the state-restore phase. State restore (auto-load
-- on launch and manual Import) doesn't expose a "restore complete" callback,
-- so we hold the watcher off for a window of frames and decay one per frame.
local SUPPRESS_RESTORE_FRAMES = 30
local function SuppressClicks(frames)
    if frames > ER_SUPPRESS_FRAMES then ER_SUPPRESS_FRAMES = frames end
end

SuppressClicks(SUPPRESS_RESTORE_FRAMES)

if ScriptHost.AddOnFrameHandler then
    ScriptHost:AddOnFrameHandler("er_suppress_decay", function(_)
        if ER_SUPPRESS_FRAMES > 0 then
            ER_SUPPRESS_FRAMES = ER_SUPPRESS_FRAMES - 1
        end
    end)
end

local function ApplyPairing(a, b)
    ER_PAIRINGS[a] = b
    ER_PAIRINGS[b] = a
    SetTargetOverlay(a, b)
    SetTargetOverlay(b, a)
    -- Restore the toggle state so the entrances show as checked, mirroring
    -- the manual pairing flow (UnlinkEntrance flips these back to false).
    local objA = Tracker:FindObjectForCode(a)
    local objB = Tracker:FindObjectForCode(b)
    if objA then objA.Active = true end
    if objB then objB.Active = true end

    -- Sync costs during state restore
    SyncSoulGateCosts(a, b)
end

local function SavePairingsFunc(self)
    -- Store one direction per pair; the load side rebuilds the symmetric mapping.
    -- The third element flags vanilla-locked pairs so they restore as locked.
    local seen = {}
    local pairs_list = {}
    for a, b in pairs(ER_PAIRINGS) do
        if not seen[a] and not seen[b] then
            seen[a] = true
            seen[b] = true
            table.insert(pairs_list, { a, b, ER_VANILLA[a] and true or false })
        end
    end
    return { Name = self.Name, Pairings = pairs_list }
end

local function LoadPairingsFunc(self, data)
    -- Re-arm the suppression window: covers our own ApplyPairing calls below
    -- AND any toggle-restore the engine fires on its side of this pass.
    SuppressClicks(SUPPRESS_RESTORE_FRAMES)
    ER_PAIRINGS = {}
    ER_VANILLA = {}
    if type(data) ~= "table" or data.Name ~= self.Name then return end
    local pairs_list = data.Pairings
    if type(pairs_list) ~= "table" then return end
    for _, entry in ipairs(pairs_list) do
        local a, b, v = entry[1], entry[2], entry[3]
        if type(a) == "string" and type(b) == "string" then
            ApplyPairing(a, b)
            if v then
                ER_VANILLA[a] = true
                ER_VANILLA[b] = true
            end
        end
    end

    if UpdateEscapeRoute then UpdateEscapeRoute() end
end

local pairings_storage = ScriptHost:CreateLuaItem()
pairings_storage.Name = "er_pairings_storage"
pairings_storage.CanProvideCodeFunc = function(self, code) return code == self.Name end
pairings_storage.SaveFunc = SavePairingsFunc
pairings_storage.LoadFunc = LoadPairingsFunc

